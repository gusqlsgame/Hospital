package board;

public class doctorDataBean {
	private String doc_code;
	private String major;
	private String doc_info;

	public String getDoc_code() {
		return doc_code;
	}

	public void setDoc_code(String doc_code) {
		this.doc_code = doc_code;
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public String getDoc_info() {
		return doc_info;
	}

	public void setDoc_info(String doc_info) {
		this.doc_info = doc_info;
	}

	public String getDoc_name() {
		return doc_name;
	}

	public void setDoc_name(String doc_name) {
		this.doc_name = doc_name;
	}

	public String getDoc_id() {
		return doc_id;
	}

	public void setDoc_id(String doc_id) {
		this.doc_id = doc_id;
	}

	public String getDoc_pass() {
		return doc_pass;
	}

	public void setDoc_pass(String doc_pass) {
		this.doc_pass = doc_pass;
	}

	private String doc_name;
	private String doc_id;
	private String doc_pass;
}
